// SOMA
function Soma (num1, num2) {
    return num1 + num2
}
console.log(Soma(10, 10))


// SUBTRAÇÃO
function Sub (num1, num2) {
    return num1 - num2
}
console.log(Sub(10, 10))


// MULTIPLICAÇÃO
function Mult (num1, num2) {
    return num1 * num2
}
console.log(Mult(10, 10))


// DIVISÃO
function Divi(num1, num2) {
    return num1 / num2
}
console.log(Divi(10, 10))


